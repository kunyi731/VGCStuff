def plot_chart(damage_array):
  fig, ax = plt.subplots()
  ax = sns.heatmap(damage_array, annot = damage_array, fmt = '', cmap="Greens",
                 xticklabels = pokemon_types, yticklabels = pokemon_types)

  ax.set_xlabel("\n\n Defending Pokémon", size = 14)
  ax.set_ylabel("Attacking Pokémon \n\n", size = 14)
  plt.title("A Pokémon Type Chart - Generation 6 \n\n", size = 18, color = "#b30000")
  plt.show()

